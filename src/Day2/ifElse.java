package Day2;

public class ifElse {
	public static void main(String[] args) {
	    int age = 17;
		if (age < 18) {
	      System.out.println("\nAge is less than 18\n\n");
	      
	    }else {
		
		System.out.println("\nAge is not less than 18\n\n ");
	}
		
		System.out.println("Out of If Block");
		
	}
}
