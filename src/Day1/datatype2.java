package Day1;

public class datatype2 {
	public static void main(String[] args) {
		byte myNum = 100;  //numbers from -128 to 127
		short myNum2 = 5000;  //numbers from -32,768 to 32,767
		int myNum3 = 100000;  //numbers from -2,147,483,648 to 2,147,483,647
		long myNum4 = 15000000000L;  //numbers from -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
		float myNum5 = 5.75f;  //storing 6 to 7 decimal digits
		double myNum6 = 19.99d;  //storing 15 decimal digits
		System.out.println(myNum);
		System.out.println(myNum2);
		System.out.println(myNum3);
		System.out.println(myNum4);
		System.out.println(myNum5);
		System.out.println(myNum6);
	}
}
